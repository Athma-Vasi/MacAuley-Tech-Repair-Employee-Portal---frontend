function returnHeadphoneProductReviewSchemas({
  headphoneDocuments,
  headphoneReviews,
  customerDocuments,
}: {
  headphoneDocuments: typeof HEADPHONE_DOCUMENTS;
  headphoneReviews: typeof HEADPHONE_REVIEWS;
  customerDocuments: typeof CUSTOMER_DOCUMENTS;
}) {
  return headphoneDocuments.flatMap((headphoneDocument) => {
    // random number of reviews
    const numberOfReviews = Math.round(Math.random() * (3 - 1) + 1);
    // shuffle customers
    const shuffledCustomers = shuffleArray(customerDocuments);
    // random slice of customers
    const randomCustomers = shuffledCustomers.slice(0, numberOfReviews);
    // shuffle reviews
    const shuffledReviews = shuffleArray(headphoneReviews);
    // random slice of reviews
    const randomReviews = shuffledReviews.slice(0, numberOfReviews);

    const productReviewSchemas: ProductReviewSchema[] = [];
    randomCustomers.forEach((customer, index) => {
      const {
        helpfulVotes,
        isVerifiedPurchase,
        productRating,
        productReview,
        unhelpfulVotes,
      } = randomReviews[index];

      const productReviewSchema: ProductReviewSchema = {
        userId: customer._id,
        username: customer.username,
        productId: headphoneDocument._id,
        productCategory: "Headphone",
        productSku: headphoneDocument.sku,
        productBrand: headphoneDocument.brand,
        productModel: headphoneDocument.model,
        productReview,
        productRating: STAR_RATINGS_OBJ[productRating],
        helpfulVotes,
        unhelpfulVotes,
        isVerifiedPurchase,
      };

      productReviewSchemas.push(productReviewSchema);
    });

    return productReviewSchemas;
  });
}

