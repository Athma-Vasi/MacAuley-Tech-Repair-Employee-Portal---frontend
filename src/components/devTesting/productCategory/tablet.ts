function returnTabletProductReviewSchemas({
  tabletDocuments,
  tabletReviews,
  customerDocuments,
}: {
  tabletDocuments: typeof TABLET_DOCUMENTS;
  tabletReviews: typeof TABLET_REVIEWS;
  customerDocuments: typeof CUSTOMER_DOCUMENTS;
}) {
  return tabletDocuments.flatMap((tabletDocument) => {
    // random number of reviews
    const numberOfReviews = Math.round(Math.random() * (3 - 1) + 1);
    // shuffle customers
    const shuffledCustomers = shuffleArray(customerDocuments);
    // random slice of customers
    const randomCustomers = shuffledCustomers.slice(0, numberOfReviews);
    // shuffle reviews
    const shuffledReviews = shuffleArray(tabletReviews);
    // random slice of reviews
    const randomReviews = shuffledReviews.slice(0, numberOfReviews);

    const productReviewSchemas: ProductReviewSchema[] = [];
    randomCustomers.forEach((customer, index) => {
      const {
        helpfulVotes,
        isVerifiedPurchase,
        productRating,
        productReview,
        unhelpfulVotes,
      } = randomReviews[index];

      const productReviewSchema: ProductReviewSchema = {
        userId: customer._id,
        username: customer.username,
        productId: tabletDocument._id,
        productCategory: "Tablet",
        productSku:
          tabletDocument.sku[Math.floor(Math.random() * tabletDocument.sku.length)],
        productBrand: tabletDocument.brand,
        productModel: tabletDocument.model,
        productReview,
        productRating: STAR_RATINGS_OBJ[productRating],
        helpfulVotes,
        unhelpfulVotes,
        isVerifiedPurchase,
      };

      productReviewSchemas.push(productReviewSchema);
    });

    return productReviewSchemas;
  });
}

