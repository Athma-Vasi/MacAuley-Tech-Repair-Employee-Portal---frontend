function returnWebcamProductReviewSchemas({
  webcamDocuments,
  webcamReviews,
  customerDocuments,
}: {
  webcamDocuments: typeof WEBCAM_DOCUMENTS;
  webcamReviews: typeof WEBCAM_REVIEWS;
  customerDocuments: typeof CUSTOMER_DOCUMENTS;
}) {
  return webcamDocuments.flatMap((webcamDocument) => {
    // random number of reviews
    const numberOfReviews = Math.round(Math.random() * (3 - 1) + 1);
    // shuffle customers
    const shuffledCustomers = shuffleArray(customerDocuments);
    // random slice of customers
    const randomCustomers = shuffledCustomers.slice(0, numberOfReviews);
    // shuffle reviews
    const shuffledReviews = shuffleArray(webcamReviews);
    // random slice of reviews
    const randomReviews = shuffledReviews.slice(0, numberOfReviews);

    const productReviewSchemas: ProductReviewSchema[] = [];
    randomCustomers.forEach((customer, index) => {
      const {
        helpfulVotes,
        isVerifiedPurchase,
        productRating,
        productReview,
        unhelpfulVotes,
      } = randomReviews[index];

      const productReviewSchema: ProductReviewSchema = {
        userId: customer._id,
        username: customer.username,
        productId: webcamDocument._id,
        productCategory: "Webcam",
        productSku: webcamDocument.sku,
        productBrand: webcamDocument.brand,
        productModel: webcamDocument.model,
        productReview,
        productRating: STAR_RATINGS_OBJ[productRating],
        helpfulVotes,
        unhelpfulVotes,
        isVerifiedPurchase,
      };

      productReviewSchemas.push(productReviewSchema);
    });

    return productReviewSchemas;
  });
}

