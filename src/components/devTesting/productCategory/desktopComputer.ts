function returnDesktopComputerProductReviewSchemas({
  desktopComputerDocuments,
  desktopComputerReviews,
  customerDocuments,
}: {
  desktopComputerDocuments: typeof DESKTOP_COMPUTER_DOCUMENTS;
  desktopComputerReviews: typeof DESKTOP_COMPUTER_REVIEWS;
  customerDocuments: typeof CUSTOMER_DOCUMENTS;
}) {
  return desktopComputerDocuments.flatMap((desktopComputerDocument) => {
    // random number of reviews
    const numberOfReviews = Math.round(Math.random() * (3 - 1) + 1);
    // shuffle customers
    const shuffledCustomers = shuffleArray(customerDocuments);
    // random slice of customers
    const randomCustomers = shuffledCustomers.slice(0, numberOfReviews);
    // shuffle reviews
    const shuffledReviews = shuffleArray(desktopComputerReviews);
    // random slice of reviews
    const randomReviews = shuffledReviews.slice(0, numberOfReviews);

    const productReviewSchemas: ProductReviewSchema[] = [];
    randomCustomers.forEach((customer, index) => {
      const {
        helpfulVotes,
        isVerifiedPurchase,
        productRating,
        productReview,
        unhelpfulVotes,
      } = randomReviews[index];

      const productReviewSchema: ProductReviewSchema = {
        userId: customer._id,
        username: customer.username,
        productId: desktopComputerDocument._id,
        productCategory: "Desktop Computer",
        productSku: desktopComputerDocument.sku,
        productBrand: desktopComputerDocument.brand,
        productModel: desktopComputerDocument.model,
        productReview,
        productRating: STAR_RATINGS_OBJ[productRating],
        helpfulVotes,
        unhelpfulVotes,
        isVerifiedPurchase,
      };

      productReviewSchemas.push(productReviewSchema);
    });

    return productReviewSchemas;
  });
}

