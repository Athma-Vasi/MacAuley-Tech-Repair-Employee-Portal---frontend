function returnKeyboardProductReviewSchemas({
  keyboardDocuments,
  keyboardReviews,
  customerDocuments,
}: {
  keyboardDocuments: typeof KEYBOARD_DOCUMENTS;
  keyboardReviews: typeof KEYBOARD_REVIEWS;
  customerDocuments: typeof CUSTOMER_DOCUMENTS;
}) {
  return keyboardDocuments.flatMap((keyboardDocument) => {
    // random number of reviews
    const numberOfReviews = Math.round(Math.random() * (3 - 1) + 1);
    // shuffle customers
    const shuffledCustomers = shuffleArray(customerDocuments);
    // random slice of customers
    const randomCustomers = shuffledCustomers.slice(0, numberOfReviews);
    // shuffle reviews
    const shuffledReviews = shuffleArray(keyboardReviews);
    // random slice of reviews
    const randomReviews = shuffledReviews.slice(0, numberOfReviews);

    const productReviewSchemas: ProductReviewSchema[] = [];
    randomCustomers.forEach((customer, index) => {
      const {
        helpfulVotes,
        isVerifiedPurchase,
        productRating,
        productReview,
        unhelpfulVotes,
      } = randomReviews[index];

      const productReviewSchema: ProductReviewSchema = {
        userId: customer._id,
        username: customer.username,
        productId: keyboardDocument._id,
        productCategory: "Keyboard",
        productSku: keyboardDocument.sku,
        productBrand: keyboardDocument.brand,
        productModel: keyboardDocument.model,
        productReview,
        productRating: STAR_RATINGS_OBJ[productRating],
        helpfulVotes,
        unhelpfulVotes,
        isVerifiedPurchase,
      };

      productReviewSchemas.push(productReviewSchema);
    });

    return productReviewSchemas;
  });
}

