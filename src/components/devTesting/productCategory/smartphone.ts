function returnSmartphoneProductReviewSchemas({
  smartphoneDocuments,
  smartphoneReviews,
  customerDocuments,
}: {
  smartphoneDocuments: typeof SMARTPHONE_DOCUMENTS;
  smartphoneReviews: typeof SMARTPHONE_REVIEWS;
  customerDocuments: typeof CUSTOMER_DOCUMENTS;
}) {
  return smartphoneDocuments.flatMap((smartphoneDocument) => {
    // random number of reviews
    const numberOfReviews = Math.round(Math.random() * (3 - 1) + 1);
    // shuffle customers
    const shuffledCustomers = shuffleArray(customerDocuments);
    // random slice of customers
    const randomCustomers = shuffledCustomers.slice(0, numberOfReviews);
    // shuffle reviews
    const shuffledReviews = shuffleArray(smartphoneReviews);
    // random slice of reviews
    const randomReviews = shuffledReviews.slice(0, numberOfReviews);

    const productReviewSchemas: ProductReviewSchema[] = [];
    randomCustomers.forEach((customer, index) => {
      const {
        helpfulVotes,
        isVerifiedPurchase,
        productRating,
        productReview,
        unhelpfulVotes,
      } = randomReviews[index];

      const productReviewSchema: ProductReviewSchema = {
        userId: customer._id,
        username: customer.username,
        productId: smartphoneDocument._id,
        productCategory: "Smartphone",
        productSku:
          smartphoneDocument.sku[
            Math.floor(Math.random() * smartphoneDocument.sku.length)
          ],
        productBrand: smartphoneDocument.brand,
        productModel: smartphoneDocument.model,
        productReview,
        productRating: STAR_RATINGS_OBJ[productRating],
        helpfulVotes,
        unhelpfulVotes,
        isVerifiedPurchase,
      };

      productReviewSchemas.push(productReviewSchema);
    });

    return productReviewSchemas;
  });
}

